"use strict";

/***********************************************************
 *
 * In which the action of accessing an object's properties,
 * i.e. its key-value pairs, is demonstrated.
 *
 **********************************************************/

var food = {                                                //  Variable 'food' defined as an object with only one key-value pair-
    types: 'only pizza',                                    //    - A string 'only pizza' value for the key 'types'
};

console.log(food.types);                                    //  Output the object property to the console (stdout)

