"use strict";

/***********************************************************
 *
 * In which the '.toString()' method is called on the number
 * object to demonstrate that a number value can be changed
 * into a string value.
 *
 **********************************************************/

var n = 128;                                                //  Variable 'n' is defined with an integer as its value.

n = n.toString();                                           //  The '.toString()' method is called on variable 'n', with the
                                                            //    resultant string being assinged to the 'n' variable.
console.log(n);                                             //  The 'n' variable, now containing the string '128' is output to
                                                            //    the console (stdout).




