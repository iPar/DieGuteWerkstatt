"use strict";

/***********************************************************
 *
 * In which the use of an object is demonstrated.
 *
 **********************************************************/

var pizza = {                                               //  Variable 'pizza' defined as an object with three key-value pairs-
    toppings: ['cheese', 'sauce', 'pepperoni'],             //    - An array of strings value for the key 'toppings'
    crust: 'deep dish',                                     //    - A string 'deep dish' value for the key 'crust'
    serves: 2                                               //    - A number key for the 'serves' key
};

console.log(pizza);                                         //  Output the second item in the array to the console (stdout)
