"use strict";

/***********************************************************
 *
 * In which the 'Math' object and its '.round()' method is
 * used to demonstrate that number variable can be rounded.
 *
 **********************************************************/

var roundUp = 1.5,                                          //  Variable 'roundUp' is defined with a float as its value
    rounded = Math.round(roundUp);                          //  Use the 'Math.round' method on the 'roundUp' float variable

console.log(rounded);                                       //  The 'rounded' number variable is logged to the console (stdout)



